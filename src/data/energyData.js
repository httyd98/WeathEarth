/**
 * Returns the key of the energy source with the highest percentage in a mix object.
 * @param {Object|null|undefined} mix
 * @returns {string|null}
 */
export function dominantEnergy(mix) {
  if (!mix) return null;
  const keys = Object.keys(mix);
  if (keys.length === 0) return null;
  return keys.reduce((best, key) => (mix[key] > mix[best] ? key : best), keys[0]);
}
